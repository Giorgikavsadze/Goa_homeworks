Grade = Number(prompt("Enter score: "))

if (Grade >= 90) {
    console.log("Grade A")
}
else if (Grade >= 80) {
    console.log("Grade B")
}
else if (Grade >= 70) {
    console.log("Grade C")
}
else if (Grade >= 60) {
    console.log("Grade D")
}
else {
    console.log("Fail")
}

console.log(Grade)